/**
 * Get lesson by ID
 */
export function getLessonById(id) {
  return securityPlusLessons.find(lesson => lesson.id === parseInt(id));
}

/**
 * Get lessons by domain
 */
export function getLessonsByDomain(domain) {
  return securityPlusLessons.filter(lesson => lesson.domain.includes(domain));
}

/**
 * Get total lesson count
 */
export function getTotalLessons() {
  return securityPlusLessons.length;
}

